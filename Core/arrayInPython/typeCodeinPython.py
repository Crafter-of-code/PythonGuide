# Python array module type codes:

# 'b' -> signed char (1 byte)
# 'B' -> unsigned char (1 byte)

# 'u' -> Unicode character (deprecated / legacy, may not be supported in modern Python)

# 'h' -> signed short (2 bytes)
# 'H' -> unsigned short (2 bytes)

# 'i' -> signed int (usually 2 or 4 bytes depending on system)
# 'I' -> unsigned int

# 'l' -> signed long
# 'L' -> unsigned long

# 'q' -> signed long long (8 bytes)
# 'Q' -> unsigned long long (8 bytes)

# 'f' -> float (4 bytes)
# 'd' -> double (8 bytes)

# '?' -> boolean (True/False)